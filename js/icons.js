/**
 * Get icon by name
 */
export function getIcon(name, size = 24) {
    const icon = ICONS[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return ICONS.file; // Fallback
    }
    return icon.replace(/width="\d+"/, `width="${size}"`).replace(/height="\d+"/, `height="${size}"`);
}

/**
 * Create icon element
 */
export function createIconElement(name, size = 24, className = '') {
    const div = document.createElement('div');
    div.innerHTML = getIcon(name, size);
    div.className = className;
    return div.firstChild;
}
